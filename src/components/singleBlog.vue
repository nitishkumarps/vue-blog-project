<template>
 <div id="single-blog">
<h1>{{blog.title}}</h1>
<article>{{blog.content}}</article>
<p>Author :{{blog.author}}</p>
<ul>
 <li v-for="(category,index) in blog.categories" :key="index">
{{category}}
 </li>
</ul>
 </div>
</template>

<script>
export default {
 data(){
  return{
   id:this.$route.params.id,
   blog:{}
  }
 },
 created(){
  this.$http.get("https://vue-blog-project-9dc82-default-rtdb.firebaseio.com/posts/"+this.id+  ".json").then(
   response=>{
    this.blog=response.data;
   }
  )
 }
}
</script>
